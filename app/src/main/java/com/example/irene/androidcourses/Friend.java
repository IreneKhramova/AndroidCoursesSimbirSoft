package com.example.irene.androidcourses;

public class Friend {
    private String avatar;
    private Long id;
    private String date;
    private String message;
    private String name;

    public Friend() {}

    public String getDate() {
        return date;
    }

    public String getMessage() {
        return message;
    }

    public String getName() {
        return name;
    }

    public Long getId() {
        return id;
    }

    public String getAvatar() {
        return avatar;
    }
}